

// Break Keyword :

// The break statement immediately stops the loop.

// Example :

// for(let j = 0; j <= 9; j++)
// {
//     if(j == 5)
//     {
//         break;
//     }
//     console.log(j);
// }

// Step-by-Step
// j = 0
// 0 == 5
// False
// Print:
// 0
// j = 1
// Print:
// 1
// j = 2
// Print:
// 2
// j = 3
// Print:
// 3
// j = 4
// Print:
// 4
// j = 5
// Condition:
// j == 5
// True
// Execute:
// break;
// Loop stops immediately.

// Output
// 0
// 1
// 2
// 3
// 4

// Notice that 5 is not printed because break terminates the loop before console.log(j) executes.