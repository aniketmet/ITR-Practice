
let day =2;
switch(day){
    case 0:
    console.log("Sunday");
    break;

    case 1:
    console.log("Monday");
    break;

    case 2:
    console.log("Tuesday");
    break;
   
    case 3:   
    console.log("Wednesday");
    break;
   
    case 4:  
    console.log("Thrusday");
    break;

    case 5:   
    console.log("Friday");
    break;

    case 6:
    console.log("Saturday");
    break;
   
    default:
    
  
    
    console.log("Invalid Day");
}

// Switch Statement in JavaScript
// The switch statement is used when you want to compare one value against multiple possible cases.
// It is often used as an alternative to long if...else if...else statements.